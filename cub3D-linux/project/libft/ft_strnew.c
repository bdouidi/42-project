#include "libft.h"

int			ft_strnew(char **str)
{
	if (!((*str) = (char *)malloc(sizeof(char) * 1)))
	{
		free((*str));
		(*str) = NULL;
		return (-1);
	}
	(*str)[0] = 0;
	return (1);
}
